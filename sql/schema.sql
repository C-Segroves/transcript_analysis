-- =============================================================================
-- schema.sql  --  Full database schema for the transcript-analysis pipeline.
--
-- This recreates the LIVE schema (as captured by inspect_schema.py): integer
-- surrogate ids, yt_* external keys, model_id-keyed scores, the island tables,
-- and all observed indexes/constraints -- PLUS the new
-- idx_vid_transcript_vid_wc index that the scoring pipeline needs.
--
-- Safe to run on an empty database (everything is IF NOT EXISTS). It does NOT
-- drop anything; to rebuild from scratch, uncomment the DROP block below first.
--
-- Notes:
--   * Indexes here are created normally (fast on an empty DB). To add an index
--     to an already-populated table without locking it, use
--     CREATE INDEX CONCURRENTLY ... in a standalone statement instead.
--   * Surrogate keys use GENERATED BY DEFAULT AS IDENTITY so the app can let the
--     DB assign ids, while bulk migrations can still insert explicit ids.
--   * Run with DBeaver (whole script) or:  psql "$CONN" -f sql/schema.sql
-- =============================================================================

